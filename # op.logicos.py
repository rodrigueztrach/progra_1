# AND  &

resultado1 = True & True
resultado2 = False & True 
resultado3 = True & False 
resultado4 = False & False 
print(f" Verdadero y Verdarero es : { resultado1 }") 
print(f" Falso y verdadero es : { resultado2 }")
print(f" Verdareo y Falso es : { resultado3 } ")
print(f" Falso y Falso es : { resultado4 }")

# not 

resultado1 = not True 
resultado2 = not False 

print (f"not true : { resultado1 }")
print (f"not false: { resultado2 }")


# OR |

respuesta1 = True  | True 
respuesta2 = False | True 
respuesta3 = True  | False
respuesta4 = False | False 
print(f' True or True  : { respuesta1 }')
print(f' False or True : { respuesta2 }')
print(f' True or False : { respuesta3 }')
print(f' False or False :{ respuesta4 }') 